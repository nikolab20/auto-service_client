    private void btnLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnLoginMouseClicked
        try {
            CommunicationController.getInstance().operationLoginWorker(txtUsername.getText(), txtPassword.getText());
            FrmClient frmClient = new FrmClient();
            frmClient.setVisible(true);
            dispose();
            JOptionPane.showMessageDialog(this.getContentPane(), "Uspesno logovanje",
                    "Uspeh", JOptionPane.INFORMATION_MESSAGE);
        } catch (IOException ex) {
            Logger.getLogger(FrmConfiguration.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(FrmConfiguration.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnLoginMouseClicked

    private void btnConnectMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnConnectMouseClicked
        try {
            if (!Controller.getInstance().isDefaultConfig()) {
                Controller.getInstance().writeIntoPropertiesFile(txtHost.getText(), txtPort.getText());
            }
            
            CommunicationController.getInstance().operationChooseLanguage(Controller.getInstance().getLocale());
            
            lblUsername.setVisible(true);
            txtUsername.setVisible(true);
            jSeparator3.setVisible(true);
            lblPassword.setVisible(true);
            txtPassword.setVisible(true);
            jSeparator4.setVisible(true);
            btnCancel2.setVisible(true);
            btnLogin.setVisible(true);
            txtUsername.setEditable(true);
            txtPassword.setEditable(true);
            
            txtHost.setEditable(false);
            txtPort.setEditable(false);
            btnCancel1.setVisible(false);
            btnConnect.setEnabled(false);
            
            JOptionPane.showMessageDialog(this.getContentPane(), "Uspesna konekcija na server",
                    "Uspeh", JOptionPane.INFORMATION_MESSAGE);
            
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, resourceBundle.getString("problemsWithFile"),
                    "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, resourceBundle.getString("problemsWithConnectionToServer"),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnConnectMouseClicked

