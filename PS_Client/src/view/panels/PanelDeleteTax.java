    /**
     * Method for panel preparation.
     */
    public void preparePanel() {
        try {
            resourceBundle = ResourceBundle.getBundle("props/LanguageBundle", Controller.getInstance().getLocale());
            poreskaStope = (List<PoreskaStopa>) CommunicationController.getInstance().operationSelectAllTax();
            tmt = new TableModelTax(poreskaStope);
            panelSearch.preparePanel(tmt);
            btnDelete.setText(resourceBundle.getString("tax_btn_delete"));
            panelSearch.addListener(this);
            panelSearch.addTableListener(this);
            btnDelete.setEnabled(false);
        } catch (Exception ex) {
            MessageDialog.showErrorMessage(null, ex.getMessage(), resourceBundle.getString("error_title"));
        }
    }

    /**
     * Method for setting panel elements on default values.
     */
    public void clearPanel() {
        try {
            poreskaStopa = null;
            poreskaStope = (List<PoreskaStopa>) CommunicationController.getInstance().operationSelectAllTax();
            panelSearch.clearPanel(new TableModelTax(poreskaStope));
            btnDelete.setEnabled(false);
        } catch (Exception ex) {
            MessageDialog.showErrorMessage(null, ex.getMessage(), resourceBundle.getString("error_title"));
        }
    }

    @Override
    public AbstractTableModel searchOdo(String criteria) {
        try {
            poreskaStope = CommunicationController.getInstance().operationSearchTax(new Long(criteria));
            MessageDialog.showSuccessMessage(null, resourceBundle.getString("tax_success_search"),
                    resourceBundle.getString("success_title"));
            return new TableModelTax(poreskaStope);
        } catch (Exception ex) {
            MessageDialog.showErrorMessage(null, ex.getMessage(), resourceBundle.getString("error_title"));
            poreskaStope.clear();
            return new TableModelTax(poreskaStope);
        }
    }

    @Override
    public void clickRow(int row) {
        poreskaStopa = poreskaStope.get(row);
        btnDelete.setEnabled(true);
    }
