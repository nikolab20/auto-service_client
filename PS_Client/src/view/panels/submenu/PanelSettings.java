    /**
     * Method for panel preparation.
     */
    public void preparePanel() {
        resourceBundle = ResourceBundle.getBundle("props/LanguageBundle", Controller.getInstance().getLocale());
        lblTitle.setText(resourceBundle.getString("settings_submenu_lbl_title"));
        btnChangeLanguage.setText(resourceBundle.getString("settings_submenu_btn_language"));
        btnAddTax.setText(resourceBundle.getString("settings_submenu_btn_add_tax"));
        btnDelete.setText(resourceBundle.getString("settings_submenu_btn_delete_tax"));

        Radnik loggedWorker = Controller.getInstance().getRadnik();

        if (!loggedWorker.isAdministrator()) {
            btnAddTax.setEnabled(false);
            btnDelete.setEnabled(false);
        }
    }

    /**
     * Method for adding listener on this panel.
     *
     * @param toAdd a object that implements FormListener interface.
     */
    public void addListener(FormListener toAdd) {
        formListeners.add(toAdd);
    }

    /**
     * A method for notifying listeners that client clicked on choose language
     * button.
     */
    private void openChooseLanguage() {
        formListeners.forEach((FormListener formListener) -> {
            formListener.openChooseLanguage();
        });
    }

    /**
     * A method for notifying listeners that client clicked on add tax rate
     * button.
     */
    private void openAddTax() {
        formListeners.forEach((formListener) -> {
            formListener.openAddTax();
        });
    }

    /**
     * A method for notifying listeners that client clicked on delete tax rate
     * button.
     */
    private void openDeleteTax() {
        formListeners.forEach((FormListener formListener) -> {
            formListener.openDeleteTax();
        });
    }
