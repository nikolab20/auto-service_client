    /**
     * Method for panel preparation.
     */
    public void preparePanel() {
        resourceBundle = ResourceBundle.getBundle("props/LanguageBundle", Controller.getInstance().getLocale());

        panelTax.setBorder(new TitledBorder(resourceBundle.getString("tax_panel_border")));
        panelTaxID.addListener(this);
        panelTaxID.getTextField().setEnabled(false);
        panelTaxID.getButton().requestFocus();
        panelTaxID.getTextField().setEditable(false);
        panelTaxID.setElementText(resourceBundle.getString("tax_btn_generate"),
                resourceBundle.getString("tax_lbl_id") + ":", "");
        panelTaxName.setElementText(resourceBundle.getString("tax_lbl_name") + ":", "");
        panelTaxValue.setElementText(resourceBundle.getString("tax_lbl_value") + ":", "");
    }

    /**
     * Method for setting panel elements on default values.
     */
    public void clearPanel() {
        panelTaxID.clearPanel();
        panelTaxName.clearPanel();
        panelTaxValue.clearPanel();
    }

    @Override
    public Object getValue() {
        Long id = Long.parseLong((String) panelTaxID.getValue());
        String name = (String) panelTaxName.getValue();
        BigDecimal value = new BigDecimal((String) panelTaxValue.getValue());

        PoreskaStopa poreskaStopa = new PoreskaStopa(id, name, value);

        return poreskaStopa;
    }

    @Override
    public void setValue(Object object) {
        PoreskaStopa poreskaStopa = (PoreskaStopa) object;

        panelTaxID.setValue(poreskaStopa.getId() + "");
        panelTaxName.setValue(poreskaStopa.getOznaka());
        panelTaxValue.setValue(poreskaStopa.getVrednost() + "");
    }

    /**
     * Method for adding listener on this panel.
     *
     * @param toAdd a object that implements GenerateListener interface.
     */
    public void addListener(GenerateListener toAdd) {
        generateListeners.add(toAdd);
    }

    @Override
    public void pressButton(ClickButtonEvent evt) {
        generateListeners.forEach((GenerateListener generateListener) -> {
            try {
                DomainObject odo = generateListener.generateOdo(new PoreskaStopa());
                panelTaxID.setValue(odo.getObjectId() + "");
            } catch (Exception ex) {
                Logger.getLogger(PanelNewCustomer.class.getName()).log(Level.SEVERE, null, ex);
            }
        });
    }
